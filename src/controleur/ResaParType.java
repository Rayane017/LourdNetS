package controleur;

public class ResaParType {
    
    private int rang; 
    private String type;
    private int nbResa;


    public ResaParType(){
    }

    public int getRang() {
        return rang;
    }

    public String getType() {
        return type;
    }

    public int getNbResa() {
        return nbResa;
    }

    public void setRang(int rang) {
        this.rang = rang;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setNbResa(int nbResa) {
        this.nbResa = nbResa;
    }

    }
